char	*ft_strcapitalize(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
			str[i] = str[i] - 32;
		if (str[i] >= 65 && str[i] <= 90)
		{
			if (str[i - 1] < 65 && str[i - 1] > 90 && i != 0)
				str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}
