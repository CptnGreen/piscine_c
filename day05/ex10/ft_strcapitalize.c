char	*ft_strcapitalize(char *str)
{
	int		i;
	int		isWord;

	i = 0;
	isWord = 1;
	while (str[i] != '\0')
	{
		{
			if (str[i] >= 97 && str[i] <= 122)
			{
				if ((str[i - 1] == ' ' || str[i - 1] == '-' || str[i - 1] == '+') || (i == 0))	
				{
						str[i] = str[i] - 32;
				}
			}
		}
		i++;
	}
	return (str);
}
