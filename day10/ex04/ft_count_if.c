int		ft_count_if(char **tab, int (*f)(char *))
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) == 1)
		{
			while (tab[i][j] != '\0')
				j++;
			return (j);
		}
		i++;
	}
	return (-1);
}
